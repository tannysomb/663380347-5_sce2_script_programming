try:
    # รับค่าและแปลงเป็นจำนวนเต็ม
    user_input = input("กรุณากรอกตัวเลขจำนวนเต็ม: ")
    num = int(user_input)

    print("--- ผลลัพธ์ ---")

    # กรณีพิเศษ: ถ้าเป็นเลข 0 ให้แสดงผลจบตรงนี้เลย ไม่ต้องไปเช็คคู่คี่ให้สับสน
    if num == 0:
        print("ตัวเลขของคุณคือ: ศูนย์ (Zero)")
    
    # กรณีตัวเลขอื่นๆ (บวก หรือ ลบ)
    else:
        # 1. เช็คว่าเป็นบวกหรือลบ
        if num > 0:
            result_sign = "จำนวนบวก"
        else:
            result_sign = "จำนวนลบ"

        # 2. เช็คว่าเป็นเลขคู่หรือเลขคี่
        if num % 2 == 0:
            result_parity = "เลขคู่"
        else:
            result_parity = "เลขคี่"

        # 🚨 เติมบรรทัดนี้ลงไปล่างสุด (เช็คย่อหน้าให้ตรงกับ if num > 0: ด้วยนะครับ)
        print(f"ตัวเลขของคุณคือ: {result_sign} และเป็น {result_parity}")

except ValueError:
    print("กรุณากรอกเฉพาะตัวเลขจำนวนเต็ม")